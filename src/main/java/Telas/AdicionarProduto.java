    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        try{
        String nome = txtnome.getText();
        String descricao = txtdescricao.getText();
        double preco = Double.parseDouble(txtpreco.getText());
        int quantidade = Integer.parseInt(txtquantidade.getText());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
       
        Produto p = new Produto();
        p.setNome(nome);
        p.setPreco(preco);
        p.setDescricao(descricao);
        p.setQuantidade(quantidade);
        
        String nomeSelecionado = (String) comboboxfornecedor.getSelectedItem(); // Obtém o nome selecionado

    if (nomeSelecionado != null) {
        Fornecedor fornecedor = FornecedorJPA.findByNome(nomeSelecionado);
        p.setFornecedor(fornecedor); // Associa o fornecedor ao produto
    }    
        
        ProdutoJPA.cadastrarProduto(p);
        JOptionPane.showMessageDialog(null,"Cadastro realizado com sucesso!");

        txtnome.setText("");
        txtpreco.setText("");
        txtquantidade.setText("");
        txtdescricao.setText("");
    }catch(Exception e){
        e.printStackTrace();
        JOptionPane.showMessageDialog(null,"Ocorreu um erro ao cadastrar Produto no sistema!");
    } 
    }//GEN-LAST:event_btnsalvarActionPerformed

